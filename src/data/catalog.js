// helper bikin episode punya id + thumb otomatis
function makeEpisodes(seriesId, list) {
  return list.map((ep) => ({
    id: `${seriesId}-ep-${ep.no}`, 
    ...ep,
    thumb: ep.thumb || `/episodes/${seriesId}/ep${ep.no}.jpg`, 
  }));
}


